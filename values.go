package main

import "fmt"

func main() {

	// string operations
	fmt.Println("go" + "lang");

	// arithmetic operations	
	fmt.Println("1 + 1 =", 1+1);
	fmt.Println("7.0/3.0 =", 7.0/3);
	fmt.Println("7/3 =", 7/3);
	
	// boolean operations	
	fmt.Println(true && false);
	fmt.Println(true || false);
	fmt.Println(!true);

}

